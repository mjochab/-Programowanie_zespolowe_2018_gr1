package pojo;

public class Specialization {
    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


    public String toString() {
        if (name.isEmpty())
            return "first contact";
        return getName();
    }
}
